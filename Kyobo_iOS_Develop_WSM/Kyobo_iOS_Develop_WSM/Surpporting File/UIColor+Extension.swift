import UIKit
extension UIColor {

  @nonobjc class var dark: UIColor {
    return UIColor(red: 40.0 / 255.0, green: 42.0 / 255.0, blue: 65.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var dustyOrange: UIColor {
    return UIColor(red: 235.0 / 255.0, green: 104.0 / 255.0, blue: 63.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var brownishGrey: UIColor {
    return UIColor(white: 111.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var grass: UIColor {
    return UIColor(red: 76.0 / 255.0, green: 172.0 / 255.0, blue: 38.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var brownGrey: UIColor {
    return UIColor(white: 135.0 / 255.0, alpha: 1.0)
  }

}
